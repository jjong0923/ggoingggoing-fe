function matchesTripType(
  route: RouteDetail,
  tripDurationType: RouteGenerateRequest["tripDurationType"],
) {
  return route.tripDurationType === tripDurationType;
}

export function getMockRoutes(): MockRouteRecord[] {
  return mockRoutes;
}

export function getMockRouteTitles(limit?: number) {
  const titles = mockRoutes.map((route) => route.title.replace(" 루트", ""));

  return typeof limit === "number" ? titles.slice(0, limit) : titles;
}

export function getMockRouteDetail(routeId: number): RouteDetail | undefined {
  return mockRoutes.find((route) => route.routeId === routeId);
}

export function getMockRoutesByRegion(regionId: number): RouteDetail[] {
  return mockRoutes.filter((route) => route.regionId === regionId);
}

export function generateMockRoute({
  contentId,
  tripDurationType,
}: RouteGenerateRequest): RouteDetail {
  const matchingRoute =
    mockRoutes.find(
      (route) =>
        route.contentId === contentId && matchesTripType(route, tripDurationType),
    ) ??
    mockRoutes.find((route) => route.contentId === contentId) ??
    mockRoutes.find((route) => matchesTripType(route, tripDurationType)) ??
    mockRoutes[0];

  return matchingRoute;
}

